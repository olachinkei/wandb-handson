"""
1_4: Playground - プレイグラウンドの活用

==========================================================
Weave Playground とは
==========================================================

Weave Playground は、UI 上でプロンプトを直接編集・テストできる機能です。
コードを書かずにプロンプトエンジニアリングができます。

==========================================================
主な機能
==========================================================

1. プロンプトの編集とテスト
   - UI 上でプロンプトを直接編集
   - 変更をすぐに実行してテスト
   - 履歴を保持

2. 複数モデルの比較
   - 複数のモデルを並列実行
   - レスポンスを横並びで比較
   - コスト・レイテンシの比較

3. パラメータ調整
   - temperature, max_tokens などを UI で調整
   - リアルタイムで結果を確認

4. トレースから Playground へ
   - 過去のトレースを Playground で開く
   - 同じ入力で別のプロンプト・モデルをテスト

==========================================================
使い方
==========================================================

1. Weave UI にアクセス
   https://wandb.ai/<entity>/<project>/weave

2. Traces タブでトレースを選択

3. "Open in Playground" をクリック

4. パラメータを変更して再実行

==========================================================
Playground の活用シーン
==========================================================

プロンプトエンジニアリング
   - システムプロンプトの調整
   - Few-shot examples の追加・削除
   - 出力フォーマットの指定

🔄 モデル比較
   - GPT-4o vs GPT-4o-mini
   - 異なる temperature での比較
   - コスト対品質のトレードオフ分析

🐛 デバッグ
   - 問題のあるトレースを再現
   - プロンプトを修正して再実行
   - 期待する出力が得られるまで調整

A/B テスト準備
   - 複数のプロンプトバリエーションを試す
   - 良いプロンプトを発見したら保存

==========================================================
Tips
==========================================================

トレースからの再現
   本番環境で問題が発生した場合、そのトレースを
   Playground で開いて同じ条件で再現・デバッグできます。

プロンプトの保存
   良いプロンプトができたら、weave.StringPrompt や
   weave.MessagesPrompt として保存できます。
   → 2_1_prompt.py を参照

評価との連携
   Playground で調整したプロンプトを、
   Evaluation で定量的に評価できます。
   → 3_1_offline_evaluation.py を参照

==========================================================
注意
==========================================================

- Playground は UI 機能のため、このスクリプトにコードはありません
- 実際に試すには Weave UI にアクセスしてください
- まず 1_1_0_basic_trace.py を実行してトレースを作成してから
  Playground で開いてみてください
"""

