"""
5_5: Saved Views - よく使う Trace / Eval の見方を保存する

==========================================================
Saved Views とは
==========================================================

Saved Views は、Traces や Evaluations の table 表示について、
filter、sort、columns、表示設定を保存して再利用する機能です。

毎回同じ条件を手で設定し直す代わりに、
目的別の view を保存しておくことで、調査やレビューをすばやく始められます。

==========================================================
何に使うか
==========================================================

1. 失敗 Trace の調査
   - error がある Trace だけを見る
   - 特定の op_name に絞る
   - latency が大きい順に並べる

2. Evaluation 結果の確認
   - 特定の scorer が低いサンプルだけを見る
   - model や dataset ごとに比較する
   - レビュー対象の列だけを表示する

3. チームでの共有
   - よく使う調査 view を保存する
   - チームメンバーが同じ条件で確認できる

4. SDK からの操作
   - Python SDK の SavedView で filter や columns を設定する
   - programmatic に view を作る

==========================================================
基本的な流れ
==========================================================

UI で使う場合:
   1. Traces または Evals の table を開く
   2. filter、sort、columns を調整する
   3. Save view で保存する
   4. 次回以降、保存した view を読み込む

SDK で使う場合:
   1. `weave.SavedView()` を作る
   2. `.set_columns()` や `.add_filter()` で条件を設定する
   3. `.save()` で保存する
   4. `.to_grid()` などで確認する

==========================================================
このハンズオンとの関係
==========================================================

Trace や Evaluation が増えてくると、毎回すべてを見るのは大変です。

Saved Views を使うと、
「失敗だけ見る」
「特定の scorer だけ見る」
「レイテンシが高いものを見る」
といった作業を再利用できます。

==========================================================
参考リンク
==========================================================

Create and manage saved views:
https://docs.wandb.ai/weave/guides/tools/saved-views

==========================================================
Note
==========================================================

- このスクリプトでは概要説明のみを表示します
- Saved Views は UI と Python SDK の両方から利用できます
"""

